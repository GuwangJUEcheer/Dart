USE brackeet;

-- ── テーブル作成 ──────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS `fortune_lucky` (
    `id`              INT          NOT NULL AUTO_INCREMENT,
    `zodiac`          VARCHAR(20)  NOT NULL UNIQUE COMMENT '星座名（日本語）',
    `lucky_stone`     VARCHAR(50)  NOT NULL COMMENT 'ラッキーストーン',
    `lucky_color`     VARCHAR(40)  NOT NULL COMMENT 'ラッキーカラー',
    `lucky_item`      VARCHAR(60)  NOT NULL COMMENT 'ラッキーアイテム',
    `lucky_direction` VARCHAR(10)  NOT NULL COMMENT 'ラッキー方位',
    `lucky_number`    TINYINT      NOT NULL COMMENT 'ラッキーナンバー',
    `precaution`      VARCHAR(120) NOT NULL COMMENT '注意事項',
    PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `fortune_message` (
    `id`       INT  NOT NULL AUTO_INCREMENT,
    `zodiac`   VARCHAR(20) NOT NULL COMMENT '星座名',
    `category` VARCHAR(20) NOT NULL COMMENT 'love/family/friendship/work/study/health',
    `content`  TEXT        NOT NULL COMMENT '運勢メッセージ',
    PRIMARY KEY (`id`),
    KEY `idx_zodiac_cat` (`zodiac`, `category`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

